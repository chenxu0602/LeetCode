        # return height(root) != -1

        def height(node):
            if node is None:
                return 0

            left, right = height(node.left), height(node.right)
            if left == -1: return -1
            if right == -1: return -1

            if abs(left - right) > 1:
                return -1

            return 1 + max(left, right)

        return height(root) != -1

        
